Steps for installation:

```sh
./install-mojo.sh
```

If permission denied:
```sh
chmod +x ./install-mojo.sh
```

After installation, it will create a new project at:
```sh
/home/user/mojo-projects/hello-world
```

You can check pixi version:

```sh
pixi --version
pixi --help # for more help
```

Running mojo project:
```sh
pixi run mojo <filename>.mojo
```
